// Performance optimization: Debounce scroll events
function debounce(func, wait) {
    let timeout;
    return function executedFunction(...args) {
        const later = () => {
            clearTimeout(timeout);
            func(...args);
        };
        clearTimeout(timeout);
        timeout = setTimeout(later, wait);
    };
}

// Apply debouncing to scroll events
const debouncedScrollHandler = debounce(() => {
    // Navbar background effect
    const navbar = document.querySelector('.navbar');
    if (window.scrollY > 50) {
        navbar.style.background = 'rgba(10, 10, 10, 0.98)';
    } else {
        navbar.style.background = 'rgba(10, 10, 10, 0.95)';
    }
    
    // Parallax effect
    const scrolled = window.pageYOffset;
    const hero = document.querySelector('.hero');
    if (hero) {
        const rate = scrolled * -0.5;
        hero.style.transform = `translateY(${rate}px)`;
    }
}, 10);

window.addEventListener('scroll', debouncedScrollHandler);

// Add loading state for project links
document.querySelectorAll('.project-link').forEach(link => {
    link.addEventListener('click', function(e) {
        if (this.href === '#') {
            e.preventDefault();
            this.style.opacity = '0.5';
            this.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Coming Soon';
            setTimeout(() => {
                this.style.opacity = '1';
                this.innerHTML = this.getAttribute('data-original') || this.innerHTML;
            }, 2000);
        }
    });
});

// Store original text for project links
document.querySelectorAll('.project-link').forEach(link => {
    link.setAttribute('data-original', link.innerHTML);
}); 